        <h3>Background of the Study</h3>
        
        <p>Background of the study is one of the preliminary stages of conducting research proposal or thesis. The background of the study is basically a review of the area being researched, current information involving the issue and relevant history regarding the issue.  
        Background of the study introduces the study concepts, definition of key variables and the current problem/context. The section shows that some research has been conducted on the area of interest, however there still exit a gap that needs to filled. The section highlights several studies that have been undertaken on the topical area summarizing their conclusive evidence and relevance to the study area..
        <img style="float: right;width:650px;" src="{{ asset('assets/img/marketing-agency/services-img3.jpg') }}" alt="image">
        </p>
         
        <p>This section can be structured using funnel approach that is worldview, regional, and local perspective. For a shorter-version concept paper this may not count however it is imperative to show whether there are studies done on this area, whether the studies a limited in terms of context, or geographical context and what are the gaps. Did the authors achieve what they set out to achieve? What need to be done to solve this gap?
        In this stage, you should do thorough preliminary research as it sets stage of what’s you are going to write in other areas. Conducting research review on your area of study and specific topic will help you to formulate a research question or thesis statement that will lead to more specific and relevant research.</p>

        <p>Writing a thesis statement will guide you and make your points authoritative. Think and write about issues, problems, or even solutions that other researchers have developed to develop your own stance regarding the issue. </p>

        <p>It is also important to create relevant sections of the background of the study. This will facilitate flow of ideas and articulation of necessary points. Conclude by identifying any further study that needs to be done in that area, or provide possible solutions to the issue that haven't been considered before.</p>

        <h4>Problem Statement Development</h4>
        <p>A problem statement is usually two to three sentences to explain the problem your process improvement project will address. In a nutshell, a problem statement will outline the negative points of the current situation and explain why this matters. It also serves as a great communication tool, helping to get buying and support from others.</p>

        <p>This section seeks to ask, what is currently happening that is off? When developing this section these are some of the important questions that you need to ask yourself:</p>
        
        <ol>
            <li>What is the statement about an area of concern?</li>
            <li>What condition needs to be improved?</li>
            <li>What is the difficulty that needs to be eliminated?</li>
            <li>What is the troubling question that exists in scholarly literature, in theory, or in practice that points to the need for meaningful understanding and deliberate investigation?</li>
            <li>Why? One of the most important goals of any problem statement is to define the problem being addressed in a way that's clear and precise. Its aim is focus the process improvement team’s activities and steer the scope of the project.</li>
        </ol>

        <p>In general, you should apply the 5 W’s (Who, what, where, when and why) to the research problem.</p>
        
        <p>Answer to any of these questions will lead to a good problem statement, but you need a structure of how to develop it; 
        And the first place to start is to highlight the importance of the topic being studied. This should orient the significance of the study and the research objectives/hypothesis/questions that will follow.</p> 
        
        <p>Then discuss the problem by placing it in the context that defines the parameters of what is to be researched. </p>
        
        <p>Answer the overarching question, so what? After you highlight and articulate the problem, what need to be done? This should convey the study importance or benefits. In other words, this paragraph should say the study is not trivial.</p>

        <h4>Justification or Rationale of the Study</h4>

        <p>It is very important to show that your research is not trivial. That your research study findings, indeed, will contribute to development of new knowledge, practice and policy development. Rationale or justification of the study should explain the importance of your research with valid and strong arguments.</p>

        <p>One way you can do this is by showing that the research will contribute to knowledge. That is, it will eliminate certain gap in the literature. Filling existing gap is the core purpose for every research study. You cannot re-invent the wheel, by coming with something totally different. There is always something that has been done about something, and the ultimate imperative of every study is to continue the development of literature about something.</p>

        <p>However, even though you don’t have to re-invent the wheel, at least you have to offer some fresh perspectives regarding, let’s say business management or social issue. In your study justification section, you need to show how your research will offer new perspectives or contribute to the existing literature regarding your study area.</p>

        <p>Another way of justifying your study, is to show how it can solve a specific problem. You have to articulate the problem in a detailed manner and show practical benefits of the study findings. Suppose, your thesis is on accelerators of derivatives market depth in Kenya. And your study found that liquidity of the underlying spot market has significant influence on the derivatives market depth. The practical solution for your study is on how to improve liquidity of the underlying spot market by practices such as bringing in market maker and many others.</p>

        <p>Finally, you can develop a rationale of your study by showing how it will contribute to your professional development. For instance, if you have aspirations to be a business manager, a study topic in strategy management can further your career and give you pre-requisite knowledge going forward. You can show how you will be in a better place career-wise after completing your study.</p>


        <h4>Scope of the Study</h4>

        <p>Owing to the fact that the field of academic is broad like an ocean or boundless like skies, it is very easy for a researcher or the reader to delve into the deluge of information that make up a discipline and get lost in the maze of it all. And that’s why there is need to have a scope of what you will cover and what you will not. Learning to eliminate is also an important skill in research project.</p>

        <p>Research study should be approached with precision. And one area to show what you are covering or the precision of your study is the scope of the study. This section contains the areas to be covered by your research. It shows the level or the amount of the object of study to be covered in your research so that you do not deviate from what you set out to achieve and also to guide the reader on what is to be expected.</p>

        <p>Scope of the study section should be approached tactfully. You need to first reiterate the problem of your research and objectives clearly. You should state the time frame of your research study. You must show the population or sample that you will be studying, the topics and theories that anchor or develops your study and the geographical location of your study. 
        You should also state the extent and length of coverage and data compilation and analysis methods. If the use of questionnaire is made in the research work you should acquaint the readers with the demographics of the participants and how many they are.</p>

        <h4>Pilot Study</h4>

        <p>You should never test the depth of a river with both feet, is an African proverb which fits the context or research pilot study. Before you dive in into the deep end of research project or thesis (data collection, analysis and interpretation of the results) you should make sure that your study is feasible. </p>

        <p>In essence, pilot study (in some cases, pre-testing) is a feasibility study that seeks to determine whether your research design, data collection and analysis methods are viable. It is a rehearsal of data collection and analysis in which you test the methods you are going to put into use. You use the results of the pilot study to improve data collection methods and guide large-scale investigation.</p>

        <p>Pilot studies are routinely incorporated in research studies because:</p>

        <ol>
            <li>They help test the proposed research design.</li>
            <li>Help redefine research questions or hypothesis.</li>
            <li>Help test the validity and reliability of the research instruments.</li>
            <li>Test the safety of clinical trials (If there any).</li>
            <li>Determine the feasibility of the study to avoid time and resource wastage.</li>
        </ol>

        <p>The following things should be considered when developing a pilot study.</p> 

        <ol>
            <li>Sample size and sample size selection. The sample of the pilot study needs to be representative of the study population or study sample size. You should use statistical techniques to calculate the required sample size. For example, you can use G-power method to determine sample size or Mugenda and Mugenda (2003) criterion.</li>
            <li>Criteria for convenient but effective pilot study participants’ selection. You should make a point of determining the right sample selection. Whether it is simple random sampling or any other method of sampling, the sampling process will entirely be determined by your proposed research design and your target population. Ensure that the process is cost effective and practical.</li>
            <li>Ethics and data collection should be also put into consideration. Ensure that your data collection processes passes all ethical considerations such as participants consent and permission to conduct the research from the university and other research authorities.</li>
            <li>Data entry and analysis. This is the critical point of your pilot study. It is at this point you are going to establish the validity and reliability of your research instruments, the power of your statistical tests and whether the sample size is sufficient enough to give the statistical tests enough power.</li>
        </ol>

        <h4>Validity and Reliability of the Research Instruments</h4>
        
        <p>Research instruments such as questionnaire and interview surveys need to be valid and reliable for a study to be a success. The study findings and conclusions of the study can only be useful if the research instruments are valid.</p> 
